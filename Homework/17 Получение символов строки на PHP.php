//1 Дана строка 'abcde'. Обращаясь к отдельным символам этой строки выведите на экран символ 'a', символ 'c', символ 'e'.
$str = 'abcde';
echo $str[0], ' ', $str[2], ' ', $str[4];

//2 Дана переменная со строкой 'abcde'.
// Обращаясь к отдельным символам этой строки запишите в новую переменную символы
// этой строки в обратном порядке, то есть 'edcba'.
$str = 'abcde';
