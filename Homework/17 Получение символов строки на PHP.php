echo $str[$num];

//Последний символ строки
/*
 * Как вы видите, количество символов в этой строке равно 5.
 * Если немного подумать, то становится очевидным, что номер последнего символа этой строки
 * будет на 1 меньше ее длины, так как нумерация символов начинается с нуля.
 */

//4 Дана строка. Выведите на экран ее последний символ.
$str = 'abcde';
